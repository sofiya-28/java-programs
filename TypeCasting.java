public class TypeCasting {
    public static void main(String[] args) {
        //implicit typecasting
        char ch='A';
        int x=ch;
        System.out.println(x);

        // Explicit typecasting
        char sofiya ='a';
        int y = (int)sofiya;
        System.out.println(y);
         
        // simple case : b assigne to s
        char s='b';
        System.out.println(s);
    }
    
}
